let UI_LANG = localStorage.getItem("de_lang") || "en";

function t(key) {
  return (I18N[UI_LANG] && I18N[UI_LANG][key]) || I18N.en[key] || key;
}

function applyI18n() {
  document.documentElement.lang = UI_LANG;
  document.documentElement.dir = UI_LANG === "fa" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.getAttribute("data-i18n-title"));
  });
}

function toggleUiLang() {
  UI_LANG = UI_LANG === "en" ? "fa" : "en";
  localStorage.setItem("de_lang", UI_LANG);
  applyI18n();
}
